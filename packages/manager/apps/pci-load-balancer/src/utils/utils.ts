import { AxiosError, isAxiosError } from 'axios';
import { nanoid } from 'nanoid';

export const getAutoGeneratedName = (prefix = ''): string => {
  const today = new Date();
  const date = `${today.getDate()}${today.getMonth() + 1}`;
  const randomNumber = nanoid(5);
  const prefixPart = prefix ? `${prefix}-` : '';

  return `${prefixPart}${date}-${randomNumber}`;
};

type CustomApiError = {
  class: string;
  message: string;
};

export const isApiErrorResponse = (
  error: unknown,
): error is AxiosError<CustomApiError> => {
  const err = error as AxiosError<CustomApiError>;
  const data = err.response?.data;

  return (
    isAxiosError(err) &&
    data?.message !== undefined &&
    data?.class !== undefined
  );
};
